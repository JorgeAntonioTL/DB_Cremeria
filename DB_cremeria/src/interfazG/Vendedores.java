    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String idVendedor, nombreV, sql;
        if(metodo == 1){
            idVendedor = jTextField1.getText();
            nombreV = jTextField2.getText();
            sql = "insert into Vendedores values('" + idVendedor + "','" + nombreV + "')";
            int band = m.agregar(sql);
            if (band != 1)
                JOptionPane.showMessageDialog(null, "Vendedor NO registrado, Revise por favor");
            else {
                JOptionPane.showMessageDialog(null, "Vendedor Registrado con Éxito ");
                jButton2.setEnabled(true);
                jButton3.setEnabled(true);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField1.setEnabled(false);
                jTextField2.setEnabled(false);
                jButton7.setEnabled(false);
                jButton8.setEnabled(false);
            }
        }else if(metodo == 2) {
            idVendedor = jTextField1.getText();
            sql = "select * from Vendedores where IdVendedor='" + idVendedor + "'";
            Object[] datos = m.buscar(sql, 2);
            if(datos[0].toString().equals("")) {
                jTextField1.setText("");
                jTextField2.setText("");
                JOptionPane.showMessageDialog(null, "Vendedor NO encontrado");
            }else {
                jTextField1.setText(datos[0].toString());
                jTextField2.setText(datos[1].toString());
                jTextField1.setEnabled(false);
                jTextField2.setEnabled(false);
                jButton4.setEnabled(true);
                jButton5.setEnabled(true);
                jButton7.setEnabled(false);
            }
        }else if(metodo == 3) {
            idVendedor = jTextField1.getText();
            nombreV = jTextField2.getText();
            sql = "update Vendedores set NombreV='" + nombreV + "' where IdVendedor='" + idVendedor + "'";
            int band = m.modificar(sql);
            if (band != 1)
                JOptionPane.showMessageDialog(null, "Vendedor NO modificado, Revise por favor");
            else {
                JOptionPane.showMessageDialog(null, "Vendedor modificado con Éxito ");
                jButton2.setEnabled(true);
                jButton3.setEnabled(true);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField1.setEnabled(false);
                jTextField2.setEnabled(false);
                jButton7.setEnabled(false);
                jButton8.setEnabled(false);
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

