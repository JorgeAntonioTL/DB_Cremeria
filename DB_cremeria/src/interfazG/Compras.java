    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String idCompra, idProveedor, fechaC, total, sql;
        if(metodo == 1){
            idCompra = jTextField1.getText();
            idProveedor = jTextField2.getText();
            fechaC = jTextField3.getText();
            total = jTextField4.getText();
            sql = "insert into Compras values('" + idCompra + "','" + idProveedor + "','" + fechaC + "'," + total + ")";
            int band = m.agregar(sql);
            if (band != 1)
                JOptionPane.showMessageDialog(null, "Compra NO registrada, Revise por favor");
            else {
                JOptionPane.showMessageDialog(null, "Compra Registrada con Éxito ");
                jButton2.setEnabled(true);
                jButton3.setEnabled(true);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField1.setEnabled(false);
                jTextField2.setEnabled(false);
                jTextField3.setEnabled(false);
                jButton7.setEnabled(false);
                jButton8.setEnabled(false);
                jButton9.setEnabled(false);
            }
        }else if(metodo == 2) {
            idCompra = jTextField1.getText();
            sql = "select * from Compras where IdCompra='" + idCompra + "'";
            Object[] datos = m.buscar(sql, 4);
            if(datos[0].toString().equals("")) {
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField1.requestFocus();
                JOptionPane.showMessageDialog(null, "Compra NO encontrada");
            }else {
                jTextField1.setText(datos[0].toString());
                jTextField2.setText(datos[1].toString());
                jTextField3.setText(datos[2].toString());
                jTextField4.setText(datos[3].toString());
                jTextField1.setEnabled(false);
                jTextField2.setEnabled(false);
                jTextField3.setEnabled(false);
                jButton4.setEnabled(true);
                jButton5.setEnabled(true);
                jButton7.setEnabled(false);
                jButton9.setEnabled(true);
            }
        }else if(metodo == 3) {
            idCompra = jTextField1.getText();
            idProveedor = jTextField2.getText();
            fechaC = jTextField3.getText();
            sql = "update Compras set IdProveedor='" + idProveedor + "', FechaC='" + fechaC + "' where IdCompra='" + idCompra + "'";
            int band = m.modificar(sql);
            if (band != 1)
                JOptionPane.showMessageDialog(null, "Compra NO modificada, Revise por favor");
            else {
                JOptionPane.showMessageDialog(null, "Compra modificada con Éxito ");
                jButton2.setEnabled(true);
                jButton3.setEnabled(true);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField1.setEnabled(false);
                jTextField2.setEnabled(false);
                jTextField3.setEnabled(false);
                jButton7.setEnabled(false);
                jButton8.setEnabled(false);
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

