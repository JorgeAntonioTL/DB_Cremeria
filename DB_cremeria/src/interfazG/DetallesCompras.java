    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int res = JOptionPane.showConfirmDialog(null, "¿Esta seguro de eliminar?", "!Alerta¡",JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        if(res == JOptionPane.YES_OPTION) {
            String idProducto = jTextField1.getText();
            String sql = "delete from DetallesCompras where IdCompra='" + idCompra + "' and IdProducto='" + idProducto + "'";
            int band = m.eliminar(sql);
            if (band != 1)
                JOptionPane.showMessageDialog(null, "Detalle NO eliminado, Revise por favor");
            else {
                JOptionPane.showMessageDialog(null, "Detalle Eliminado Exitosamente ");
                jButton2.setEnabled(true);
                jButton3.setEnabled(true);
                jButton4.setEnabled(false);
                jButton5.setEnabled(false);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField1.setEnabled(false);
                jTextField2.setEnabled(false);
                jButton7.setEnabled(false);
                jButton8.setEnabled(false);
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

