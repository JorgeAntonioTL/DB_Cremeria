    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String idProducto, cantidadPC, costoP, subTotalC, sql;
        if(metodo == 1){
            idProducto = jTextField1.getText();
            cantidadPC = jTextField2.getText();
            costoP = jTextField3.getText();
            
            sql = "select Stock from Productos where IdProducto='" + idProducto + "'";
            Object stock = m.buscarA(sql, "Stock");
            sql = "select CantidadP from Productos where IdProducto='" + idProducto + "'";
            Object cant = m.buscarA(sql, "CantidadP");
            if(!stock.toString().isEmpty()){
                if((Double.parseDouble(stock.toString()) + Double.parseDouble(cantidadPC)) <= Double.parseDouble(cant.toString())){
                    sql = "update Productos set Stock=" + (Double.parseDouble(stock.toString()) + Double.parseDouble(cantidadPC)) + " where IdProducto='" + idProducto + "'";
                    m.modificar(sql);
                    subTotalC = String.valueOf((Double.parseDouble(cantidadPC) * Double.parseDouble(costoP)));
                    sql = "insert into DetallesCompras values('" + idCompra + "','" + idProducto + "'," + cantidadPC + "," + costoP + "," + subTotalC + ")";
                    int band = m.agregar(sql);
                    if (band != 1)
                        JOptionPane.showMessageDialog(null, "Detalle NO registrado, Revise por favor");
                    else {
                        JOptionPane.showMessageDialog(null, "Detalle Registrado con Éxito ");
                        jButton2.setEnabled(true);
                        jButton3.setEnabled(true);
                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextField1.setEnabled(false);
                        jTextField2.setEnabled(false);
                        jTextField3.setEnabled(false);
                        jButton7.setEnabled(false);
                        jButton8.setEnabled(false);
                    }
                }else {
                    JOptionPane.showMessageDialog(null, "No es posible agregar la compra por exceso del producto");
                }
            }else {
                    JOptionPane.showMessageDialog(null, "Producto no encontrado");
            }
        }else if(metodo == 2) {
            idProducto = jTextField1.getText();
            sql = "select * from DetallesCompras where IdCompra='" + idCompra + "' and IdProducto='" + idProducto + "'";
            Object[] datos = m.buscarD(sql, 4);
            if(datos[0].toString().equals("")) {
                jTextField1.setText("");
                jTextField2.setText("");
                JOptionPane.showMessageDialog(null, "Detalle NO encontrado");
            }else {
                jTextField1.setText(datos[0].toString());
                jTextField2.setText(datos[1].toString());
                jTextField3.setText(datos[2].toString());
                jTextField4.setText(datos[3].toString());
                jTextField1.setEnabled(false);
                jTextField2.setEnabled(false);
                jTextField3.setEnabled(false);
                jButton4.setEnabled(true);
                jButton5.setEnabled(true);
                jButton7.setEnabled(false);
            }
        }else if(metodo == 3) {
            idProducto = jTextField1.getText();
            cantidadPC = jTextField2.getText();
            costoP = jTextField3.getText();
            
            sql = "select Stock from Productos where IdProducto='" + idProducto + "'";
            Object stock = m.buscarA(sql, "Stock");
            sql = "select CantidadP from Productos where IdProducto='" + idProducto + "'";
            Object cant = m.buscarA(sql, "CantidadP");
            sql = "select CantidadPC from DetallesCompras where IdCompra='" + idCompra + "' and IdProducto='" + idProducto +"'";
            Object cantPC = m.buscarA(sql, "CantidadPC");
            if(!stock.toString().isEmpty()){
                if((Double.parseDouble(stock.toString()) + Double.parseDouble(cantidadPC) - Double.parseDouble(cantPC.toString())) <= Double.parseDouble(cant.toString())){
                    sql = "update Productos set Stock=" + (Double.parseDouble(stock.toString()) + Double.parseDouble(cantidadPC) - Double.parseDouble(cantPC.toString())) + " where IdProducto='" + idProducto + "'";
                    m.modificar(sql);
                    subTotalC = String.valueOf(Double.parseDouble(cantidadPC) * Double.parseDouble(costoP));
                    sql = "update DetallesCompras set CantidadPC=" + cantidadPC + ", CostoP=" + costoP + ", SubTotalC=" + subTotalC + " where IdCompra='" + idCompra + "' and IdProducto='" + idProducto + "'";
                    int band = m.modificar(sql);
                    if (band != 1)
                        JOptionPane.showMessageDialog(null, "Detalle NO modificado, Revise por favor");
                    else {
                        JOptionPane.showMessageDialog(null, "Detalle modificado con Éxito ");
                        jButton2.setEnabled(true);
                        jButton3.setEnabled(true);
                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextField4.setText("");
                        jTextField1.setEnabled(false);
                        jTextField2.setEnabled(false);
                        jTextField3.setEnabled(false);
                        jTextField4.setEnabled(false);
                        jButton7.setEnabled(false);
                        jButton8.setEnabled(false);
                    }
                }else {
                    JOptionPane.showMessageDialog(null, "No es posible modificar la compra por exceso del producto");
                }
            }else {
                    JOptionPane.showMessageDialog(null, "Producto no encontrado");
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

